package com.yue.yueaiagent.tools;

import cn.hutool.http.HttpUtil;
import cn.hutool.json.JSONArray;
import cn.hutool.json.JSONObject;
import cn.hutool.json.JSONUtil;
import org.springframework.ai.tool.annotation.Tool;
import org.springframework.ai.tool.annotation.ToolParam;

import java.util.HashMap;
import java.util.List;
import java.util.Map;
import java.util.stream.Collectors;

/**
 * 网页搜索工具
 */
public class WebSearchTool {

    // SearchAPI 的搜索接口地址
    private static final String SEARCH_API_URL = "https://www.searchapi.io/api/v1/search";

    private final String apiKey;

    public WebSearchTool(String apiKey) {
        this.apiKey = apiKey;
    }

    @Tool(description = "Search for information from Baidu Search Engine")
    public String searchWeb(@ToolParam(description = "Search query keyword") String query) {
        Map<String, Object> paramMap = new HashMap<>();
        paramMap.put("q", query);
        paramMap.put("api_key", apiKey);
        paramMap.put("engine", "baidu");
        try {
            String response = HttpUtil.get(SEARCH_API_URL, paramMap);
            JSONObject jsonObject = JSONUtil.parseObj(response);
            JSONArray organicResults = jsonObject.getJSONArray("organic_results");

            if (organicResults == null || organicResults.isEmpty()) {
                return "No results found for: " + query;
            }

            // --- 核心修复：安全截取前 5 条 ---
            int end = Math.min(organicResults.size(), 5);
            List<Object> objects = organicResults.subList(0, end);

            // 拼接结果（建议只取关键字段，节省 Token）
            return objects.stream().map(obj -> {
                JSONObject item = (JSONObject) obj;
                return String.format("标题: %s\n链接: %s\n摘要: %s\n",
                        item.getStr("title"), item.getStr("link"), item.getStr("snippet"));
            }).collect(Collectors.joining("\n---\n"));

        } catch (Exception e) {
            return "Error searching Baidu: " + e.getMessage();
        }
    }
}
